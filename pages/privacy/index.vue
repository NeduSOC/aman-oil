<script setup>
  import { ref, onMounted } from "vue";
  import { gsap } from "gsap";
  import { ScrollTrigger } from "gsap/ScrollTrigger";
  import { useSeoMeta } from "#app";

  // Register GSAP plugins
  gsap.registerPlugin(ScrollTrigger);

  // Template refs
  const header = ref(null);
  const content = ref(null);
  const contactSection = ref(null);
  const lastUpdated = ref(null);

  // Section refs for staggered animation
  const section1 = ref(null);
  const section2 = ref(null);
  const section3 = ref(null);
  const section4 = ref(null);
  const section5 = ref(null);
  const section6 = ref(null);
  const section7 = ref(null);
  const section8 = ref(null);
  const section9 = ref(null);

  // SEO Meta
  useSeoMeta({
    title: "Privacy Policy - AMAN Blessed Naturals",
    description:
      "Learn how AMAN Blessed Naturals collects, uses, and protects your personal information. Read our comprehensive privacy policy.",
    keywords:
      "privacy policy, data protection, AMAN Blessed Naturals, personal information, cookies",
    ogTitle: "Privacy Policy - AMAN Blessed Naturals",
    ogDescription:
      "Our commitment to protecting your privacy and personal information.",
    ogType: "website",
  });

  const animatePrivacyPage = ref(null);

  // Animation function
  const setupAnimation = () => {
    animatePrivacyPage.value = () => {
      const tl = gsap.timeline();

      // Animate header
      tl.fromTo(
        header.value,
        { y: 30, opacity: 0 },
        { y: 0, opacity: 1, duration: 0.8, ease: "power2.out" }
      );

      // Animate main content container
      tl.fromTo(
        content.value,
        { y: 50, opacity: 0, scale: 0.95 },
        { y: 0, opacity: 1, scale: 1, duration: 1, ease: "power3.out" },
        "-=0.4"
      );

      // Animate sections with stagger
      const sections = [
        section1.value,
        section2.value,
        section3.value,
        section4.value,
        section5.value,
        section6.value,
        section7.value,
        section8.value,
        section9.value,
      ].filter(Boolean);

      tl.fromTo(
        sections,
        { y: 30, opacity: 0 },
        {
          y: 0,
          opacity: 1,
          duration: 0.6,
          stagger: 0.1,
          ease: "power2.out",
        },
        "-=0.6"
      );

      // Animate contact section
      tl.fromTo(
        contactSection.value,
        { y: 20, opacity: 0 },
        { y: 0, opacity: 1, duration: 0.6, ease: "power2.out" },
        "-=0.3"
      );

      // Animate last updated
      tl.fromTo(
        lastUpdated.value,
        { opacity: 0 },
        { opacity: 1, duration: 0.4, ease: "power2.out" },
        "-=0.2"
      );

      return tl;
    };
  };

  onMounted(() => {
    setupAnimation();
    // Set up scroll-triggered animation
    ScrollTrigger.create({
      trigger: header.value,
      start: "top 80%",
      onEnter: () => {
        animatePrivacyPage.value();
      },
      once: true,
    });
  });
</script>

